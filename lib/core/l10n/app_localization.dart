  String t(String key) {
    final lang = _localizedValues[locale.languageCode] ?? _localizedValues[fallbackLanguageCode]!;
    return lang[key] ??
        _localizedValues[fallbackLanguageCode]![key] ??
        key;
  }

  String tr(String key, Map<String, String> params) {
    var value = t(key);
    params.forEach((k, v) {
      value = value.replaceAll('{$k}', v);
    });
    return value;
  }

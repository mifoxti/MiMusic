  String t(String key) {
    final lang = _localizedValues[locale.languageCode] ?? _localizedValues[fallbackLanguageCode]!;
    return lang[key] ??
        _localizedValues[fallbackLanguageCode]![key] ??
        key;
  }

  String tr(String key, Map<String, String> params) {
    var value = t(key);
    params.forEach((k, v) {
      value = value.replaceAll('{$k}', v);
    });
    return value;
  }
}

extension AppLocalizationBuildContextX on BuildContext {
  AppLocalization get l10n {
    final locale = Localizations.localeOf(this);
    return AppLocalization(locale);
  }

  String t(String key) => l10n.t(key);
  String tr(String key, Map<String, String> params) => l10n.tr(key, params);
}
